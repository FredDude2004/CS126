int main()
{
    int t[5], *p, i;

    p = t;
    
    for ( i=0 ; i<5 ; i++ )
    {
        *p = i ;
        p++;
    }
    
    for(i=0; i<5; i++)
    {
        printf("t[%d] = %d\n", i, t[i]);
    }
    
    printf("t is at %p, p holds value %p and points to %d\n", t, p, *p); 
    printf("(p+2)[0] = %d\n", (p+2)[0]); printf("(p-2)[4] = %d\n", (p-2)[4]);
    printf("(p+10)[-8] = %d\n", (p+10)[-8]);
    printf("(p+*p)[*(p+1)] = %d\n", (p+*p)[*(p+1)]);
    
    return 0;
}